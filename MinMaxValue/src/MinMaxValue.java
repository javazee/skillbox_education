public class MinMaxValue {
    public static void main(String[] args) {
        byte minByte = Byte.MIN_VALUE;
        byte maxByte = Byte.MAX_VALUE;
        short minShort = Short.MIN_VALUE;
        short maxShort = Short.MAX_VALUE;
        int minInt = Integer.MIN_VALUE;
        int maxInt = Integer.MAX_VALUE;
        long minLong = Long.MIN_VALUE;
        long maxLong = Long.MAX_VALUE;
        float minFloat = Float.MIN_VALUE;
        float maxFloat = Float.MAX_VALUE;
        double minDouble = Double.MIN_VALUE;
        double maxDouble = Double.MAX_VALUE;
        System.out.println("Минимальное значение примитива byte: " + minByte);
        System.out.println("Максимальное значение примитива byte: " + maxByte);
        System.out.println("Минимальное значение примитива short: " + minShort);
        System.out.println("Максимальное значение примитива short: " + maxShort);
        System.out.println("Минимальное значение примитива int: " + minInt);
        System.out.println("Максимальное значение примитива int: " + maxInt);
        System.out.println("Минимальное значение примитива long: " + minLong);
        System.out.println("Максимальное значение примитива long: " + maxLong);
        System.out.println("Минимальное значение примитива float: " + minFloat);
        System.out.println("Максимальное значение примитива float: " + maxFloat);
        System.out.println("Минимальное значение примитива double: " + minDouble);
        System.out.println("Максимальное значение примитива double: " + maxDouble);
    }
}
